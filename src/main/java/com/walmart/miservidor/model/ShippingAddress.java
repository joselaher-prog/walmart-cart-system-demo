package com.walmart.miservidor.model;

public class ShippingAddress {
    private String street, city, zoneId;
    
    public String getStreet() { return street; } public void setStreet(String s) { street = s; }
    public String getCity() { return city; } public void setCity(String c) { city = c; }
    public String getZoneId() { return zoneId; } public void setZoneId(String z) { zoneId = z; }
}